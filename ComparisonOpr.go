package main

import "fmt"

func main() {
	number1 := 5
	number2 := 10

	// Comparasion Operator
	fmt.Println(number1 > number2)
	fmt.Println(number1 < number2)
	fmt.Println(number1 >= number2)
	fmt.Println(number1 <= number2)
	fmt.Println(number1 == number2)
	fmt.Println(number1 != number2)

	// Boolean Comparasion Operator
	con1 := true
	con2 := false
	fmt.Println(con1 || con2)
	fmt.Println(con1 && con2)
	fmt.Println(!con1)

}
